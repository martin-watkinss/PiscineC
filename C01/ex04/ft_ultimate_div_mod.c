void	ft_ultimate_div_mod(int	*a, int	*b)
{
	int	tempo;

	if (b != 0)
	{
		tempo = *a / *b;
		*b = *a % *b;
		*a = tempo;
	}
	else
	{
		*a = 0;
		*b = 0;
	}
}
